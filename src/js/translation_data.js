$("ul.lang li").click(function (e) {
  var lang = $(this).data("lang");
  console.log(lang);
  console.log($("ul.lang").data("page"));
  localStorage.setItem("bo-lang", lang);

  $("ul.lang li").removeClass("active");
  $(this).addClass("active");

  translate_main(lang);

  switch ($("ul.lang").data("page")) {
    case "home":
      translate_home(lang);
      break;
    case "ovcar":
      translate_ovcar(lang);
      break;
    case "pedigree":
      translate_pedigree(lang);
      break;
  }
});

// NOTIFICATION
var expirey_date = "2021-03-25T10:20:30Z";

if (new Date().getTime() < new Date(expirey_date).getTime()) {
  $("header").append(
    '<div style="display:none" class="newsbar"><span class="news">X</span><span class="news-close">✕</span></div>'
  );

  showNews();

  $(".news-close").click(hideNews);
}

function showNews() {
  if (sessionStorage.getItem("hideNews")) {
    return;
  }
  var _lang = localStorage.getItem("bo-lang");
  $(".news").text(translation[_lang || "hr"].news);

  $("body").addClass("offset");
  $(".newsbar").fadeIn(150);
}

function hideNews() {
  $(".newsbar").fadeOut(150);
  $("body").removeClass("offset");
  sessionStorage.setItem("hideNews", true);
}
